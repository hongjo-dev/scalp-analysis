import 'dart:io';

class AnalysisResult {
  final File imageFile;
  final int folliculitis;
  final int fineScaling;
  final int dandruff;
  final int hairLoss;
  final int excessSebum;
  final String scalpCondition;

  AnalysisResult({
    required this.imageFile,
    required this.folliculitis,
    required this.fineScaling,
    required this.dandruff,
    required this.hairLoss,
    required this.excessSebum,
    required this.scalpCondition,
  });
}
